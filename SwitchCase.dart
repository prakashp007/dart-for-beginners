void main(){
    //Switch case statements : Applicable for only Int or string


    String grade = 'A';
    switch(grade){
      case 'A':
        print("Grade A");
        break;
      case 'B':
        print("Grade B");
        break;
      case 'C':
        print("Grade C");
        break;
      case 'D':
        print("Grade D");
        break;
      default:
        print("In Valid Case");
        
    } 

}